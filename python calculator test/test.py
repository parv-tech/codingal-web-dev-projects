num1 = int(input("Enter a number : "))

num2 = int(input("Enter another number :"))

sign = str(input("Enter a sign in words : ")) 

 
if (sign == "plus") :
    a = num1 + num2
    print(a)

elif (sign == "minus") :
    b = num1 - num2
    print(b)

elif (sign == "multiply") : 
    c = num1 * num2
    print(c)

elif (sign == "divide") : 
    d = num1 / num2
    print(d)

    
    
    